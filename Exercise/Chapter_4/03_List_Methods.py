sam_li = [7,9,6,5,4,2,56,74,41]

# Sorting the list
sam_li.sort()
print(sam_li)

# Reversing the list
sam_li.reverse()
print(sam_li)

# Appending the list
sam_li.append(23)
print(sam_li)

# Inserting the value in the list
sam_li.insert(4,786)            # Insert the value at position 4
print(sam_li)

# Poping the element from the list
sam_li.pop(2)                   # Removes the element from the position 2
print(sam_li)

